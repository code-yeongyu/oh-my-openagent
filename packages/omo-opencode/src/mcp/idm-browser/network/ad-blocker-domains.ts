export function isAdBlockerDomain(host: string): boolean {
  if (!host) return false
  const lower = host.toLowerCase()
  if (AD_BLOCKER_DOMAINS.has(lower)) return true
  const parts = lower.split(".")
  for (let i = 1; i < parts.length - 1; i++) {
    const candidate = parts.slice(i).join(".")
    if (AD_BLOCKER_DOMAINS.has(candidate)) return true
  }
  return false
}

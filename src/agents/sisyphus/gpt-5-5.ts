对于较大任务，在有助于理解时，最多使用两三个高级别章节。按面向用户的结果或主要更改区域分组，而不是按文件或编辑清单。如果答案开始变成变更日志，压缩它：削减逐文件细节、重复框架、低信号回顾和可选的后续想法，然后才削减结果、验证或真实风险。


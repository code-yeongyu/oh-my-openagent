/**
 * Get the AGENTS.md template for a specific architecture layer
 */
export function getLayerTemplate(
  architecture: ArchitecturePattern,
  layer: ArchitectureLayer,
  projectPath: string,
): string | null {
  // Try to load from templates
  const templatePath = `architectures/${architecture.id}/${layer.agentsMd}`;
  return loadTemplate(templatePath, projectPath);
}

/**
 * Generate a layer-specific AGENTS.md file
 */
export function generateLayerAgentsMd(
  layer: ArchitectureLayer,
  architecture: ArchitecturePattern,
  config: InitProjectConfig,
  projectPath: string,
): string {
  // Try to load template
  const template = getLayerTemplate(architecture, layer, projectPath);
  
  if (template) {
    // Template exists, use it as-is (it's already customized for this layer)
    return template;
  }

  // No template found, generate a generic one
  return generateGenericLayerAgentsMd(layer, architecture, config);
}

/**
 * Generate a generic layer AGENTS.md when no template exists
 */
function generateGenericLayerAgentsMd(
  layer: ArchitectureLayer,
  architecture: ArchitecturePattern,
  config: InitProjectConfig,
): string {
  const { projectInfo, techStack } = config;
  const layerNameCapitalized = layer.name.charAt(0).toUpperCase() + layer.name.slice(1);

  // Determine primary language for code examples
  const primaryLang = techStack.languages.find((l) => l.primary)?.name || "TypeScript";
  const langLower = primaryLang.toLowerCase();

  return `# ${layerNameCapitalized} Layer

> Instructions for the **${layer.name}** layer of ${projectInfo.name}

## Purpose

${layer.description}

## Architecture Context

This layer is part of the **${architecture.name}** pattern.

**Path**: \`${layer.path}/\`

## Rules

### DO ✅

1. Keep ${layer.name} focused on their core responsibility
2. Follow established patterns in existing code
3. Write comprehensive tests for new code
4. Document complex business logic
5. Use dependency injection for external dependencies

### DON'T ❌

1. Don't mix concerns from other layers
2. Don't access database directly from this layer (if not data layer)
3. Don't bypass the established architecture
4. Don't create circular dependencies
5. Don't expose internal implementation details

## File Structure

\`\`\`
${layer.path}/
├── index.${langLower === "typescript" || langLower === "javascript" ? "ts" : langLower === "python" ? "py" : langLower}     # Public exports
├── types.${langLower === "typescript" || langLower === "javascript" ? "ts" : langLower === "python" ? "py" : langLower}     # Type definitions
└── __tests__/  # Tests for this layer
\`\`\`

## Patterns

### Creating New Files

When creating new files in this layer:

1. Follow the naming convention: \`kebab-case\`
2. Export from the \`index\` file
3. Include tests alongside the implementation
4. Add JSDoc/docstring comments for public APIs

### Example

\`\`\`${langLower === "typescript" ? "typescript" : langLower === "javascript" ? "javascript" : langLower}
// Example pattern for ${layer.name}
${getExampleCode(layer, primaryLang)}
\`\`\`

## Testing

- Write unit tests for all public functions
- Use mocks for external dependencies
- Aim for >80% coverage in this layer
- Test edge cases and error conditions

## Dependencies

This layer follows the ${architecture.name} dependency rules.
See the architecture documentation for allowed imports.

## Related

- **Project Overview**: \`/AGENTS.md\`
- **Architecture Docs**: \`.opencode/templates/architectures/${architecture.id}/\`
- **Project Context**: \`.opencode/project-context.yaml\`
`;
}


// MAIN GENERATION FUNCTION
// =============================================================================

export async function generateAgentsMdFiles(
  projectPath: string,
  config: InitProjectConfig,
): Promise<string[]> {
  const createdFiles: string[] = [];
  const { architecture } = config;

  // Generate root AGENTS.md
  const rootAgentsMd = generateRootAgentsMd(config);
  const rootPath = path.join(projectPath, "AGENTS.md");
  fs.writeFileSync(rootPath, rootAgentsMd);
  createdFiles.push("AGENTS.md");

  // Generate layer-specific AGENTS.md files
  for (const layer of architecture.layers) {
    const layerDir = path.join(projectPath, layer.path);
    
    // Create directory if it doesn't exist
    if (!fs.existsSync(layerDir)) {
      fs.mkdirSync(layerDir, { recursive: true });
    }

    // Generate AGENTS.md
    const layerAgentsMd = generateLayerAgentsMd(layer, architecture, config, projectPath);
    const layerPath = path.join(layerDir, "AGENTS.md");
    fs.writeFileSync(layerPath, layerAgentsMd);
    createdFiles.push(`${layer.path}/AGENTS.md`);
  }

  return createdFiles;
}


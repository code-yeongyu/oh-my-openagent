/**
 * AGENTS.md Generator
 * 
 * Generates root AGENTS.md and architecture-specific AGENTS.md files
 * based on templates and project configuration.
 */

import * as fs from "fs";
import * as path from "path";
import type { TechStack, ProjectInfo } from "./tech-detection";
import type { InitProjectConfig, ArchitecturePattern, ArchitectureLayer } from "./config-generator";

// =============================================================================
// TEMPLATE LOADING
// =============================================================================

const TEMPLATES_PATH = ".opencode/templates";

export function loadTemplate(templatePath: string, projectPath: string): string | null {
  const fullPath = path.join(projectPath, TEMPLATES_PATH, templatePath);
  if (fs.existsSync(fullPath)) {
    return fs.readFileSync(fullPath, "utf-8");
  }
  return null;
}

// =============================================================================
// ROOT AGENTS.MD GENERATION
// =============================================================================

export function generateRootAgentsMd(config: InitProjectConfig): string {
  const { projectInfo, projectType, techStack, architecture, linear, mintlify } = config;
  const now = new Date().toISOString();

  // Languages section
  const languagesSection = techStack.languages.map((lang) => {
    let entry = `- **${lang.name}**`;
    if (lang.version) entry += ` v${lang.version}`;
    if (lang.primary) entry += ` (primary)`;
    return entry;
  }).join("\n");

  // Frameworks section
  let frameworksSection = "";
  if (techStack.frameworks.frontend) {
    frameworksSection += `- **Frontend**: ${techStack.frameworks.frontend.name}`;
    if (techStack.frameworks.frontend.version) {
      frameworksSection += ` v${techStack.frameworks.frontend.version}`;
    }
    frameworksSection += "\n";
  }
  if (techStack.frameworks.backend) {
    frameworksSection += `- **Backend**: ${techStack.frameworks.backend.name}`;
    if (techStack.frameworks.backend.version) {
      frameworksSection += ` v${techStack.frameworks.backend.version}`;
    }
    frameworksSection += "\n";
  }

  // Testing section
  let testingSection = "";
  if (techStack.frameworks.testing.length > 0) {
    testingSection = "### Testing Frameworks\n";
    for (const test of techStack.frameworks.testing) {
      testingSection += `- **${test.name}**`;
      if (test.version) testingSection += ` v${test.version}`;
      testingSection += "\n";
    }
  }

  // Database section
  let databaseSection = "";
  if (techStack.databases.length > 0) {
    databaseSection = "### Databases\n";
    for (const db of techStack.databases) {
      databaseSection += `- **${db.type}**`;
      if (db.name) databaseSection += ` (${db.name})`;
      if (db.orm) databaseSection += ` - ORM: ${db.orm}`;
      databaseSection += "\n";
    }
  }

  // Architecture layers
  const layersSection = architecture.layers.map((layer) => 
    `- \`${layer.path}/\` - ${layer.description}`
  ).join("\n");

  // Linear section
  let linearSection = "";
  if (linear.enabled) {
    linearSection = `### Linear (Project Management)
- **Team**: ${linear.teamName || linear.teamId || "Configured"}
${linear.projectId ? `- **Project**: ${linear.projectName || linear.projectId}` : ""}

#### Linear Workflow
- All significant work must have a Linear issue
- Get branch names from Linear before starting work
- ⚠️ Issues are REQUIRED for all work`;
  } else {
    linearSection = `### Linear (Project Management)
Linear integration not configured. Enable in \`.opencode/project-context.yaml\`.`;
  }

  // Mintlify section
  let mintlifySection = "";
  if (mintlify.enabled) {
    mintlifySection = `### Documentation
- **Platform**: Mintlify
- **Docs Path**: \`${mintlify.docsPath}\`
- **Config**: \`mint.json\`

API and architecture documentation should be maintained in Mintlify.`;
  } else {
    mintlifySection = `### Documentation
- All documentation in local markdown files
- Use AGENTS.md files for layer-specific instructions`;
  }

  return `# Project Instructions

> This file provides project-wide instructions for all AI agents.
> Generated by \`init-project\` command. Customize as needed.

## Project Overview

**Name**: ${projectInfo.name}
**Type**: ${projectType}
**Description**: ${projectInfo.description || "No description provided"}

## Technology Stack

### Languages
${languagesSection}

### Frameworks
${frameworksSection || "- None configured"}

${testingSection}

${databaseSection}

### Package Manager
- **${techStack.packageManager || "Not specified"}**

## Architecture

**Pattern**: ${architecture.name}

### Project Structure
${layersSection}

See architecture-specific AGENTS.md files in each layer directory for detailed instructions.

## Integrations

${linearSection}

${mintlifySection}

## Coding Conventions

### Naming
| Element | Convention |
|---------|------------|
| Files | \`kebab-case\` |
| Functions | \`camelCase\` |
| Classes | \`PascalCase\` |
| Constants | \`SCREAMING_SNAKE_CASE\` |
| Components | \`PascalCase\` |

### Code Style
- **Indentation**: 2 spaces
- **Quotes**: double
- **Semicolons**: yes
- **Trailing Commas**: yes
- **Max Line Length**: 100 characters

### Commits
**Format**: \`conventional\`

Pattern: \`<type>(<scope>): <description>\`

Example: \`feat(auth): add OAuth2 integration [LINEAR-123]\`

Valid scopes: \`feat\`, \`fix\`, \`docs\`, \`style\`, \`refactor\`, \`test\`, \`chore\`

### Branch Naming
Pattern: \`{type}/{issue-id}-{description}\`

Example: \`feature/LINEAR-123-add-user-auth\`

## Workflow Rules

### 1. Before Starting Work
${linear.enabled ? `- [ ] Ensure Linear issue exists (or create one)
- [ ] Get branch name from Linear issue` : ""}
- [ ] Read relevant AGENTS.md files for the areas you'll modify
- [ ] Pull latest changes from \`main\`
- [ ] Create feature branch following naming convention

### 2. During Work
- [ ] Follow architecture patterns defined in layer AGENTS.md files
- [ ] Keep commits atomic and well-described
- [ ] Update documentation as you code
- [ ] Add tests for new functionality
- [ ] Run linting and formatting before committing

### 3. After Completing Work
${linear.enabled ? `- [ ] Update Linear issue status
- [ ] Link PR to Linear issue` : ""}
- [ ] Create PR against \`main\`
- [ ] Ensure all CI checks pass
- [ ] Request code review
- [ ] Update AGENTS.md if architectural patterns changed

## References

- **Project Context**: \`.opencode/project-context.yaml\`
- **Architecture Templates**: \`.opencode/templates/architectures/\`
- **Agent Definitions**: \`.opencode/agent/\`
- **Instructions**: \`.opencode/instructions/\`

---

*This file was generated by OpenCode. Version: 1.0.0*
*Last updated: ${now}*
`;
}

